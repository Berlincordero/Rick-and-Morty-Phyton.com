#Operadores lógicos: and, or, not
#El and, or y not son operadores lógicos que actúan sobre valores booleanos.


#AND
#es verdadero si y solo si todos los operandos son verdaderos.

print(True and False) # aqui observamos que ambas variables son diferentes
                      #por lo tanto no es verdadero y nos devolvera un False
#Ahora veamos cuando es verdadero

print(True and True) # aqui vemos que ambos valores son verdaderos esto nos retornara un True

#OR
#devuelve verdadero si al menos un elemento es verdadero. 

print(True or False) 

print(False or False)
                     
#NOT
#el not invierte verdadero por falso y viceversa.

print(not True) 

print(not False)
                     